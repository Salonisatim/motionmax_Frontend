        let currentCat = 'all';

        function toggleMenu() { document.getElementById('navLinks').classList.toggle('active'); }

        function renderGrid(list) {
            const grid = document.getElementById('courseGrid');
            grid.innerHTML = list.map(c => `
                <div class="c-card" onclick="openModal('${c.title}')">
                    <div class="c-img" style="background-image: url('${c.img}')">
                        <div class="c-badge">${c.cat}</div>
                    </div>
                    <div class="c-content">
                        <h3>${c.title}</h3>
                        <p>${c.desc.substring(0, 60)}...</p>
                        <a href="javascript:void(0)" style="color:var(--primary); font-weight:800; text-decoration:none; display:inline-block; margin-top:15px;">View Details →</a>
                    </div>
                </div>
            `).join('');
        }

        function openModal(courseTitle) {
            const course = courses.find(c => c.title === courseTitle);
            if(course) {
                document.getElementById('modalTitle').innerText = course.title;
                document.getElementById('modalCat').innerText = course.cat;
                document.getElementById('modalDuration').innerHTML = `<i class="far fa-clock"></i> ${course.duration}`;
                document.getElementById('modalDesc').innerText = course.desc;
                document.getElementById('modalSoftware').innerText = course.software || "Standard Industry Tools";
                document.getElementById('modalMode').innerText = course.mode || "Contact for details";
                document.getElementById('modalImg').style.backgroundImage = `url('${course.img}')`;
                
                // Load Career list
                const careerList = document.getElementById('modalCareers');
                if(course.careers) {
                    careerList.innerHTML = course.careers.map(job => `<li><i class="fas fa-briefcase"></i> ${job}</li>`).join('');
                } else {
                    careerList.innerHTML = `<li><i class="fas fa-check-circle"></i> Industry Standard Career Path</li>`;
                }
                
                const modal = document.getElementById('courseModal');
                modal.style.display = 'flex';
                setTimeout(() => modal.classList.add('active'), 10);
                document.body.style.overflow = 'hidden';
            }
        }

        function closeModal() {
            const modal = document.getElementById('courseModal');
            modal.classList.remove('active');
            setTimeout(() => {
                modal.style.display = 'none';
                document.body.style.overflow = 'auto';
            }, 300);
        }

        function setCategory(cat, btn) {
            currentCat = cat;
            document.querySelectorAll('.f-btn').forEach(b => b.classList.remove('active'));
            if(btn) btn.classList.add('active');
            searchCourses();
        }

        function filterBySub(subName) {
            document.getElementById('courseSearch').value = subName;
            searchCourses();
            if(window.innerWidth <= 768) toggleMenu();
        }

        function searchCourses() {
            const query = document.getElementById('courseSearch').value.toLowerCase();
            const filtered = courses.filter(c => {
                const matchesSearch = c.title.toLowerCase().includes(query);
                const matchesCat = currentCat === 'all' || c.cat === currentCat;
                return matchesSearch && matchesCat;
            });
            renderGrid(filtered);
        }

        window.onload = () => renderGrid(courses);
  